    "9. 结论包装为共识（conclusionAsProtocol）\n"
    "   表现：频繁用「这告诉我们...」「由此可见...」「不难发现...」「说到底...」来包装结论。\n"
    "   AI喜欢在每个分析后面加一句「总结式定论」，像老师在批改作业。\n"
    "   人类特征：人会自然地给出结论——直接说结论本身，不加「这告诉我们」的包装纸。\n"
    "   定位方法：搜索「这告诉」「由此」「不难发现」「说到底」「归根结底」。\n"

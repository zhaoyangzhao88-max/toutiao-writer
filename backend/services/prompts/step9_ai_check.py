def build_user_message(article: str) -> str:
    msg = "文章全文：\n"
    msg += article
    msg += "\n\n请逐条扫描以上文章的22个AI信号特征，找出确实存在的信号并给出具体位置和修改建议。输出完整JSON报告。"
    return msg

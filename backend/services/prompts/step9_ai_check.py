    "    定位方法：感受全文情绪变化是否有「意外」。如果每段的情绪功能可以被准确标注，就是信号。\n"
    "\n"
    "21. 二元对立简化（binarySimplification）\n"
    "    表现：把复杂问题简化为两个对立面。如「穷人思维vs富人思维」「打工者vs创业者」「传统行业vs互联网」\n"
    "    AI喜欢二元对立，因为它降低了理解成本，但也降低了思考深度。\n"
    "    定位方法：搜索是否频繁出现「A vs B」「X思维vs Y思维」的对立框架。\n"
    "\n"
    "22. 抽象概念拟人化（abstractPersonification）\n"
    "    表现：「时代在呼唤...」「历史选择了...」「市场做出了回应...」「命运给了我们...」\n"
    "    把抽象概念当成人来写，赋予其「意志」和「选择」，这是AI非常高频的修辞习惯。\n"
    "    定位方法：搜索抽象名词后跟「呼唤」「选择」「回应」「告诉」等拟人动词的组合。\n"

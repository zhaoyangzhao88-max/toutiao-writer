    "    虽然这是头条文章的常见写法，但AI特别喜欢用且用得千篇一律。\n"
    "    定位方法：看最后一句是否为反问句。\n"
    "\n"
    "16. 过度总结（overSummarizing）\n"
    "    表现：文章有多个总结段——中间总结一次，结尾又总结一次。\n"
    "    AI喜欢在每个大段结尾总结，再在全篇结尾总结。\n"
    "    定位方法：检查是否有重复的总结性段落。\n"
    "\n"
    "17. 金句堆砌（goldenQuoteStacking）\n"
    "    表现：密集堆砌「金句」——每段都有一句试图被截屏分享的「名言」。\n"
    "    人类特征：一篇好文章有1-2个自然冒出来的金句就够了。刻意每段造金句是AI的强迫症。\n"
    "    定位方法：检查是否有超过3句「像名言」的句子。\n"
    "\n"
    "18. 无具体人名（noSpecificNames）\n"
    "    表现：全文提到人物时只用「一位专家」「一位老板」「某个大V」「有业内人士」等模糊指代。\n"
    "    人类特征：人写自己知道的真实人物时，会自然地写出名字、职位、具体背景。\n"
    "    定位方法：搜索「一位」「某个」「有业者」等模糊指代。\n"
    "\n"
    "19. 数据无出处（dataWithoutSource）\n"
    "    表现：引用了数据但没有说明来源。\n"
    "    人类特征：人引用数据时会说「根据XX报告」「XX机构的数据显示」。即使不记得具体来源，也会说「记得之前看过一个数据」。\n"
    "    定位方法：找所有带数字的句子，检查其后是否有出处说明。\n"
    "\n"
    "20. 情感递进模板化（templatedEmotionProgression）\n"
    "    表现：情绪走向完全可预测——开头震惊、中间共鸣、高潮愤怒/感动、结尾回味。\n"

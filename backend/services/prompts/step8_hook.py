def build_user_message(article: str) -> str:
    msg = "文章全文：\n"
    msg += article
    msg += "\n\n请为以上文章生成三种不同写法的开篇版本（数据冲击、悬念前置、场景代入各一），并完成六维度质量检查。"
    return msg

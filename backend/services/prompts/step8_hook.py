    "写法要点：\n"
    "- 选最反常识、最极端、最「不可能」的数字\n"
    "- 数字后面紧跟一句「这意味着什么」的解读\n"
    "- 数字要精确（9,514不是「近万」，翻3倍不是「大幅增长」）\n"
    "- 如果文章没有具体数据，创造对比数字（「你以为是A，其实是B」）\n"
    "模板：{震撼数字} + {这意味着什么} + {拉回读者自身}\n"
    "示例：\n"
    "「1300万骑手，单价从10元跌到3元，只用了3年。你的外卖越来越便宜，他们越来越难。」\n"
    "「38%——这是中国居民消费占GDP的比例，全球倒数。不是中国人不爱花钱，是算明白了。」\n"
    "\n"
    "### 方法二：悬念前置（suspense）\n"
    "先藏结论，用一个反常识的问题或场景把读者钩住。\n"
    "为什么有效：人类大脑对「未完成的信息」有强迫症般的补齐冲动。\n"
    "写法要点：\n"
    "- 问一个读者自己回答不了、但又很想知道答案的问题\n"
    "- 描述一个让人困惑的场景，但不解释原因\n"
    "- 别急着给答案——悬念的核心是「延迟满足」\n"
    "- 问题要具体，不能太宽泛（「为什么穷人越来越穷」太宽，效果差）\n"
    "模板：{反常识现象/问题} + {暗示有深层原因} + {但不立刻揭晓}\n"
    "示例：\n"
    "「同一个小区的二手房，装修更好、楼层更高，挂牌价却比隔壁低了30万。买家看完两套，毫不犹豫选了贵的。为什么？」\n"
    "「全国最火的奶茶店，创始人自己从不喝奶茶。他说了一句话，让所有加盟商沉默了。」\n"
    "\n"
    "### 方法三：场景代入（scene_immersion）\n"
    "描绘一个读者一看就「对，我也经历过」的场景，让读者瞬间代入。\n"
    "为什么有效：人对自己经历过的事格外关注，场景唤起记忆→记忆唤起情绪→情绪驱动继续阅读。\n"
    "写法要点：\n"
    "- 用具体的感官细节（声音、画面、触感），不是抽象描述\n"
    "- 场景要足够日常、足够普遍（大多数人经历过）\n"
    "- 场景里要埋一个「不对劲」的细节，作为钩子\n"
    "- 第一人称或第二人称（「你」「我」），不要第三人称\n"
    "模板：{日常场景+具体细节} + {一个反常的细节} + {引出话题}\n"
    "示例：\n"
    "「凌晨1点，你第8次刷新朋友圈，看到前同事发了张加班照，配文'这个点还在公司的都是战士'。你刚要划走，突然发现照片角落的电脑屏幕是黑的。」\n"
    "「周五晚上8点，你坐在工位上，周围人都在'摸鱼'——看剧、刷淘宝、发呆。没人下班。不是忙，是不敢第一个走。」\n"
    "\n"
    "## 开篇写法禁忌（三种方法通用）\n"
    "1. 禁止用「众所周知」「近年来」「随着发展」「在当今社会」开头\n"
    "2. 禁止用「笔者」「本文」「下面我来」等论文用语\n"
    "3. 禁止超过100字还在绕弯子——第1句就必须有料\n"
    "4. 禁止三种方法都写成同一种感觉——必须能清晰分辨方法差异\n"
    "5. 禁止照抄原标题——开篇是独立创作，标题是另一个存在\n"
    "\n"
    "## 六维度质量检查（每个版本都要逐项检查）\n"
    "\n"
    "1. 独立性（independence）：这个开头能独立存在吗？不依赖标题也能让人想继续看？\n"
    "   - pass：不依赖标题上下文，自己就是完整钩子\n"

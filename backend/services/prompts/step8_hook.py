    "   - fail：第一句平淡，需要读到后面才有感觉\n"
    "\n"
    "3. 悬念度（suspense）：读者会想知道「然后呢？」吗？\n"
    "   - pass：创造了明确的信息缺口，必须往下读才能补齐\n"
    "   - fail：开头已经把结论说完了，或者根本没有悬念\n"
    "\n"
    "4. 可信度（credibility）：像真的吗？会不会觉得是标题党？\n"
    "   - pass：数据或场景真实可信，没有夸大感\n"
    "   - fail：有明显夸张、编造感，读者第一反应是「假的吧」\n"
    "\n"
    "5. 口语友好（oralFriendly）：读出声来像人在说话吗？\n"
    "   - pass：自然的口语节奏，像朋友在跟你讲一件事\n"
    "   - fail：像在念稿子/背课文/做汇报\n"
    "\n"
    "6. 匹配度（matching）：开头的风格和正文一致吗？\n"
    "   - pass：开头定调与正文内容自然衔接，不违和\n"
    "   - fail：开头很炸但正文接不住，读者感觉被耍了\n"
    "\n"
    "## 推荐逻辑\n"
    "给三个版本中综合六维度表现最好的标 recommended: true。\n"
    "推荐优先级：钩子力 > 口语友好 > 匹配度 > 悬念度 > 可信度 > 独立性\n"

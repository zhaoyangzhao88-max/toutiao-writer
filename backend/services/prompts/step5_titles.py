def build_user_message(material_card: dict, deconstruct_result: dict) -> str:
    import json
    msg = "素材卡：\n" + json.dumps(material_card, ensure_ascii=False) + "\n\n"
    if deconstruct_result:
        msg += "概念拆解结果：\n" + json.dumps(deconstruct_result, ensure_ascii=False) + "\n\n"
    msg += "请生成3-5个备选标题，至少覆盖3种不同的心理触发器。"
    return msg
